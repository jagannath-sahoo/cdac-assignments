package jerin;

class Study {
		 private String name;
		 private int rollNo;
		 private float [] marks;
		 
		 public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getRollNo() {
			return rollNo;
		}
		public void setRollNo(int rollNo) {
			this.rollNo = rollNo;
		}
		public float[] getMarks() {
			return marks;
		}
		public void setMarks(float[] marks) {
			this.marks = marks;
		}
		
		
	
}
